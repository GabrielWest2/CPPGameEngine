Skybox::Skybox(std::vector<std::string> faces) : skyboxTexture(faces), skyboxShader("skybox.vert", "skybox.frag")
{

    GLuint vbo;
    glGenBuffers(1, &vbo);
    glBindBuffer(GL_ARRAY_BUFFER, vbo);
    glBufferData(GL_ARRAY_BUFFER, 3 * 36 * sizeof(float), &skyboxVertices, GL_STATIC_DRAW);


    glGenVertexArrays(1, &VAO);
    glBindVertexArray(VAO);
    glEnableVertexAttribArray(0);
    glBindBuffer(GL_ARRAY_BUFFER, vbo);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 0, NULL);
}

void Skybox::Draw(Camera camera)
{
    glDisable(GL_CULL_FACE);
    glDepthMask(GL_FALSE);
    skyboxShader.Activate();
    
    camera.StationaryMatrix(skyboxShader, "camera");

   
    glBindVertexArray(VAO);
    glBindTexture(GL_TEXTURE_CUBE_MAP, skyboxTexture.ID);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    glDepthMask(GL_TRUE);
    glEnable(GL_CULL_FACE);

}
